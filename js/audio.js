// ============================
// audio.js（BGM・SE の管理）
// ============================
// このファイルはゲーム全体の音を一元管理する。
// ・BGM（ループ）
// ・タイプ音（正解）
// ・ミス音
// ・ビーム音
// ・音量スライダーとの同期
// ============================


// ------------------------------------------------------------
// BGM（ループ再生）
// ------------------------------------------------------------
const bgm = new Audio("sounds/bgm.mp3");
bgm.loop = true;          // ループ再生
bgm.volume = 0.5;         // 初期音量（50%）

// ------------------------------------------------------------
// タイプ音（正解）
// ------------------------------------------------------------
const typeGood = new Audio("sounds/type_good.mp3");
typeGood.volume = 0.6;

// ------------------------------------------------------------
// ミス音
// ------------------------------------------------------------
const typeMiss = new Audio("sounds/type_miss.mp3");
typeMiss.volume = 0.6;

// ------------------------------------------------------------
// ビーム音
// ------------------------------------------------------------
const beam = new Audio("sounds/beam.mp3");
beam.volume = 0.8;


// ------------------------------------------------------------
// 音量をまとめて変更する関数
// ------------------------------------------------------------
// タイトル画面とゲーム画面のスライダーから呼ばれる
// ------------------------------------------------------------
function setVolume(v) {
  bgm.volume = v;
  typeGood.volume = v;
  typeMiss.volume = v;
  beam.volume = v;
}


// ------------------------------------------------------------
// BGM を再生する関数
// ------------------------------------------------------------
function playBGM() {
  // 自動再生ブロック対策：再生失敗しても無視
  bgm.play().catch(() => {});
}


// ------------------------------------------------------------
// 外部から使えるように export
// ------------------------------------------------------------
export const audio = {
  playBGM,
  setVolume,
  typeGood: () => typeGood.play(),
  typeMiss: () => typeMiss.play(),
  beam: () => beam.play()
};
